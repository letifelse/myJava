public class If {
    public static void main(String[] args) {
        int myInt = 15;
        if (myInt > 10) {
            System.out.println("Hey, dude");
        } else if (myInt < 20) {
            System.out.println("NO, dude");
        } else {
            System.out.println("Whatever");
        }
    }
}

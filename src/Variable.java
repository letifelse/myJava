public class Variable {
    public static void main(String[] args) {
        int myInt = 557; //целое число 32bit
        short myShort = 3266; //16bit int
        long mylong = 321654654; //64bit int

        //вещественные типы данных
        double myDouble = 235.3; //16bit
        float myFloat = 385.3f; //32bit, если хотите именно float, в конце ставится f.

        char c = 'a'; //символьный тип данных, символ всегда пишется в одинарных кавычках
        boolean b = true; // boolean

        byte myByte = 100; // 8bit информации или 1 байт, тип данных "байт" от -128 до 127

        System.out.println(myInt);
    }
}

public class Strings {
    public static void main(String[] args) {

        String str = "Hello"; //примитивные типы данных пишутся с маленькой буквы, ссылочные типы данных = класс.
        // если класс - это чертеж, то объект - это то, что по этому чертежу было построено.
        String space = " ";
        String name = "Bob";
        System.out.println(str + space + name);

    }
}
